text = 'Я {} uchenik '

print(text.upper()) #увеличивает все буквы
print(text.lower()) #уменьшает все буквы
print(text.title()) #увеличивает каждую букву в слове
print(text.capitalize()) #увеличивает только первую букву
print(text.replace('Ит', '100')) #пишеться другое слово 
print(text.find("Я")) #находит её цифру в тектсе
print(text.index("Я")) #находит её цифру
print(text.count("Я")) #считает сколко тех иных выбранных букв
print(text.split()) #
